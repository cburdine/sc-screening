from typing import Optional, List, Dict, Union

class Element:
    symbol: str
    name: Optional[str]
    n_protons: Optional[int]
    n_neutrons: Optional[int]
    n_electrons: Optional[int]
    atomic_number: Optional[int]
    period: Optional[int]
    group: Optional[int]
    atomic_mass: Optional[float]
    atomic_radius: Optional[float]
    first_ionization_energy: Optional[float]
    element_type: Optional[str]

    def __init__(self,
                 symbol: str,
                 name: Optional[str] = None,
                 n_protons: Optional[int] = None,
                 n_neutrons: Optional[int] = None,
                 n_electrons: Optional[int] = None,
                 atomic_number: Optional[int] = None,
                 period: Optional[int] = None,
                 group: Optional[int] = None,
                 atomic_mass: Optional[float] = None,
                 atomic_radius: Optional[float] = None,
                 first_ionization_energy: Optional[float] = None,
                 element_type: Optional[str] = None):
        self.symbol = symbol
        self.name = name
        self.n_protons = n_protons
        self.n_neutrons = n_neutrons
        self.n_electrons = n_electrons
        self.atomic_number = atomic_number
        self.period = period
        self.group = group
        self.atomic_mass = atomic_mass
        self.atomic_radius = atomic_radius
        self.first_ionization_energy = first_ionization_energy
        self.element_type = element_type
    
    def __repr__(self):
        return self.symbol

    def __eq__(self, other):
        if isinstance(other, self.__class__):
            return self.symbol == other.symbol
        elif isinstance(other, str):
            return self.symbol == other
        else:
            return False

    def __hash__(self):
        return self.symbol.__hash__()

