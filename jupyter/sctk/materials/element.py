class ElementTable():

    def __init__(self, elements: List[Element]):
        self._symbol_dict = {
            elem.symbol : elem for elem in elements
        }

        self._name_dict = {
            elem.name : elem for elem in elements
        }

        self._number_dict = {
            elem.atomic_number : elem for elem in elements
        }

    def items(self):
        return self._symbol_dict.items()

    def keys(self):
        return self._symbol_dict.keys()

    def values(self):
        return self._symbol_dict.values()

    def items_by_number(self):
        return self._number_dict.items()

    def items_by_name(self):
        return self._name_dict.items()

    def elements(self):
        return list([ 
            self._number_dict[i] 
            for i in sorted(self._number_dict.keys())
        ])

    def __iter__(self):
        yield from self.elements()

    def __len__(self):
        return len(self._symbol_dict)

    def __contains__(self, key):
        if isinstance(key, int):
            return (key in self._number_dict)
        elif isinstance(key, Element):
            return self._number_dict[key.symbol]
        return (key in self._symbol_dict)

    def __getitem__(self,key):
        if isinstance(key, int):
            return self._number_dict[key]
        if isinstance(key, Element):
            return self._symbol_dict[key.symbol]
        return self._symbol_dict[key]

